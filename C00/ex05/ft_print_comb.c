#include <unistd.h>

//void ft_print_comb(void);

void	write_n(char a, char b, char c)
{
	if (a == '7')
		write(1, "789", 3);
	else
	{
		write(1, &a, 1);
		write(1, &b, 1);
		write(1, &c, 1);
		write(1, ", ", 2);
	}
}

void	ft_print_comb(void)
{
	int	a;
	int	b;
	int	c;

	a = '0';
	b = '1';
	c = '2';
	while (a <= '7')
	{
		b = a + 1;
		while (b <= '8')
		{
			c = b + 1;
			while (c <= '9')
			{
				write_n(a, b, c);
				c++;
			}
			b++;
		}
		a++;
	}
}

