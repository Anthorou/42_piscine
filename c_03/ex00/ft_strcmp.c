#include <unistd.h>
#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] < s2[i])
			return (-1);
		else if (s1[i] > s2[i])
			return (1);
		i++;
	}
	return (0);
}

int main(void)
{
	char s1[] = "on a un examen aujourd'hui.";
	char s2[] = "allo les amis comment aller vous?";
	int a;

	a = ft_strcmp(s1, s2);

	printf("%d", a);
}
